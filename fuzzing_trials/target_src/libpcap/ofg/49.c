int LLVMFuzzerTestOneInput_49(const uint8_t *data, size_t size) {
    char tmpl[] = "/tmp/fuzzfileXXXXXX";
    int fd = mkstemp(tmpl);
    if (fd == -1) {
        return 0;
    }

    if (write(fd, data, size) != size) {
        close(fd);
        unlink(tmpl);
        return 0;
    }

    close(fd);

    char errbuf[PCAP_ERRBUF_SIZE];
    u_int precision = 0; // Choose a default precision value

    // Call the function-under-test
    pcap_t *handle = pcap_open_offline_with_tstamp_precision(tmpl, precision, errbuf);

    if (handle != NULL) {
        pcap_close(handle);
    }

    unlink(tmpl);
    return 0;
}

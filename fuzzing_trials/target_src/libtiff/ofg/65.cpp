extern "C" int LLVMFuzzerTestOneInput_65(const uint8_t *data, size_t size) {
    // Create a temporary file to write the fuzz data
    char tmpl[] = "/tmp/fuzzfileXXXXXX";
    int fd = mkstemp(tmpl);
    if (fd == -1) {
        return 0;
    }

    // Write the fuzz data to the temporary file
    if (write(fd, data, size) != (ssize_t)size) {
        close(fd);
        return 0;
    }

    // Ensure the file descriptor is at the beginning for reading
    lseek(fd, 0, SEEK_SET);

    // Define the parameters for TIFFFdOpenExt
    const char *mode = "r"; // Open the file in read mode
    TIFFOpenOptions *options = TIFFOpenOptionsAlloc(); // Allocate default options

    // Call the function-under-test
    TIFF *tiff = TIFFFdOpenExt(fd, tmpl, mode, options);

    // Clean up
    if (tiff != nullptr) {
        TIFFClose(tiff);
    }
    TIFFOpenOptionsFree(options);
    close(fd);
    unlink(tmpl); // Remove the temporary file

    return 0;
}

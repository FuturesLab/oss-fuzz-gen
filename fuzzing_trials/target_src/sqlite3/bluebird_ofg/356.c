#include <stdint.h>
#include <stddef.h>
#include "sqlite3.h"
#include <string.h>
#include <stdlib.h> // Include for malloc and free

int LLVMFuzzerTestOneInput_356(const uint8_t *data, size_t size) {
    // Ensure the data is null-terminated for use as a string
    char *error_msg = (char *)malloc(size + 1);
    if (error_msg == NULL) {
        return 0;
    }
    memcpy(error_msg, data, size);
    error_msg[size] = '\0';

    // Open an in-memory SQLite database
    sqlite3 *db;
    if (sqlite3_open(":memory:", &db) != SQLITE_OK) {
        free(error_msg);
        return 0;
    }

    // Execute a simple SQL statement using the fuzzed input
    char *errmsg = NULL;
    sqlite3_exec(db, error_msg, 0, 0, &errmsg);

    // Free any error message generated by sqlite3_exec
    if (errmsg != NULL) {
        sqlite3_free(errmsg);
    }

    // Close the database connection
    sqlite3_close(db);

    // Free allocated memory
    free(error_msg);

    return 0;
}